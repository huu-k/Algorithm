'''
X가 3으로 나누어 떨어지면, 3으로 나눈다.
X가 2로 나누어 떨어지면, 2로 나눈다.
1을 뺀다.'''

#d[i] = min(x//2, x//3    )+1

n = int(input())
d = [0]*10000001
for i in range(2, n+1 ):
    #1을 빼는 경우 
    d[i] = d[i-1]+1
    
    #3으로 나눠질 때
    if i%3==0:
        d[i] = min(d[i], d[i//3]+1)
    #2로 나눠질때
    if i%2 == 0:
        d[i] = min(d[i], d[i//2]+1)
    
print(d[n])
        