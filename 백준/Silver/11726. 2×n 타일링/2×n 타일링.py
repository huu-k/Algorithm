'''
입력
첫째 줄에 n이 주어진다. (1 ≤ n ≤ 1,000)

출력
첫째 줄에 2×n 크기의 직사각형을 채우는 방법의 수를 10,007로 나눈 나머지를 출력한다.

d[i] = d[i-1]+d[i-2]

'''

n = int(input())

d= [0]*(1001)
d[1]=1
d[2]=2
if n<= 2:
    print(d[n])

else:
    for i in range(3, n+1):
        d[i] = d[i-1]+d[i-2]
    
    print(d[n]%10007)






